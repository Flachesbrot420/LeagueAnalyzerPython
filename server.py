@app.route('/api/gold_timeline')
def gold_timeline():
    """Average gold per minute across all games."""
    db = get_session()
    try:
        puuid = get_tracked_puuid(db)
        if not puuid:
            return jsonify({"error": "No tracked account found"})

        # Map each timeline info_id to the tracked participantId
        tracked_participants = {
            pt.info_id: pt.participantId
            for pt in db.query(ParticipantTimeLineDto).filter(ParticipantTimeLineDto.puuid == puuid).all()
        }

        frames = db.query(FramesTimeLineDto)\
            .filter(FramesTimeLineDto.info_id.in_(tracked_participants.keys()))\
            .order_by(FramesTimeLineDto.timestamp)\
            .all()

        timeline = {}
        for frame in frames:
            minute = frame.timestamp // 60000
            if not frame.participant_frames:
                continue
            for pf in frame.participant_frames.participant_data:
                if tracked_participants.get(frame.info_id) != pf.participantId:
                    continue
                if minute not in timeline:
                    timeline[minute] = {"gold": 0, "count": 0}
                timeline[minute]["gold"]  += pf.totalGold or 0
                timeline[minute]["count"] += 1

        result = [
            {"minute": m, "avg_gold": round(timeline[m]["gold"] / timeline[m]["count"])}
            for m in sorted(timeline.keys())
            if timeline[m]["count"] > 0
        ]
        return jsonify(result)
    finally:
        db.close()

