@app.route('/api/summary')
def summary():
    """Overall stats: total games, win rate, avg KDA."""
    db = get_session()
    try:
        puuid = get_tracked_puuid(db)
        if not puuid:
            return jsonify({"error": "No tracked account found"})

        participants = db.query(ParticipantDto).filter(ParticipantDto.puuid == puuid).all()
        if not participants:
            return jsonify({"error": "No data found for tracked account"})

        total  = len(participants)
        wins   = sum(1 for p in participants if p.win)
        kills  = sum(p.kills or 0 for p in participants)
        deaths = sum(p.deaths or 0 for p in participants)
        assists= sum(p.assists or 0 for p in participants)

        # Average KDA per game for the tracked player
        avg_kda = round(sum((p.kills or 0 + p.assists or 0) / max(p.deaths or 0, 1) for p in participants) / total, 2)

        return jsonify({
            "total_games": total,
            "wins": wins,
            "losses": total - wins,
            "win_rate": round(wins / total * 100, 1),
            "avg_kills":   round(kills   / total, 1),
            "avg_deaths":  round(deaths  / total, 1),
            "avg_assists": round(assists / total, 1),
            "avg_kda": avg_kda
        })
    finally:
        db.close()

@app.route('/api/champions')
def champions():
    """Per-champion stats: games, wins, avg KDA."""
    db = get_session()
    try:
        puuid = get_tracked_puuid(db)
        if not puuid:
            return jsonify({"error": "No tracked account found"})

        participants = db.query(ParticipantDto).filter(ParticipantDto.puuid == puuid).all()
        champ_data = {}

        for p in participants:
            name = p.championName or "Unknown"
            if name not in champ_data:
                champ_data[name] = {"games": 0, "wins": 0, "kills": 0, "deaths": 0, "assists": 0}
            c = champ_data[name]
            c["games"]   += 1
            c["wins"]    += 1 if p.win else 0
            c["kills"]   += p.kills   or 0
            c["deaths"]  += p.deaths  or 0
            c["assists"] += p.assists or 0

        result = []
        for name, c in champ_data.items():
            g = c["games"]
            kda = round((c["kills"] + c["assists"]) / max(c["deaths"], 1), 2)
            result.append({
                "champion":  name,
                "games":     g,
                "wins":      c["wins"],
                "win_rate":  round(c["wins"] / g * 100, 1),
                "avg_kills":   round(c["kills"]   / g, 1),
                "avg_deaths":  round(c["deaths"]  / g, 1),
                "avg_assists": round(c["assists"] / g, 1),
                "kda": kda
            })

        result.sort(key=lambda x: x["games"], reverse=True)
        return jsonify(result)
    finally:
        db.close()

@app.route('/api/recent')
def recent():
    """Last 10 matches with basic info."""
    db = get_session()
    try:
        infos = db.query(MatchInfoDto).order_by(MatchInfoDto.gameCreation.desc()).limit(10).all()
        puuid = get_tracked_puuid(db)
        if not puuid:
            return jsonify({"error": "No tracked account found"})
        result = []
        for info in infos:
            target = next((p for p in info.participants if p.puuid == puuid), None)
            if not target and info.participants:
                target = info.participants[0]  # fallback
            if not target:
                continue
            result.append({
                "match_id":      info.gameId,
                "champion":      target.championName,
                "win":           target.win,
                "kills":         target.kills,
                "deaths":        target.deaths,
                "assists":       target.assists,
                "cs":            (target.totalMinionsKilled or 0) + (target.neutralMinionsKilled or 0),
                "vision_score":  target.visionScore,
                "game_duration": info.gameDuration,
                "game_mode":     info.gameMode,
                "map_id":        info.mapId,
                "queue_id":      info.queueId,
            })
        return jsonify(result)
    finally:
        db.close()

